void solve() {
  int n;
  cin >> n;

  int c1, c2;
  c2 = n / 3;
  c1 = n - 2 * c2;
  if (n % 3 == 2) {
    c2 += 1;
    c1 -= 2;
  }

  cout << c1 << " " << c2 << "\n";
}

